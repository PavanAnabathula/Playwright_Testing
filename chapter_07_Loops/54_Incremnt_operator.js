//Loops helps us to repeat the block of code without we writing

//Increment Operator

// ++ --> increases the value by 1

//Pre-Increment()

let a =10;
let b= ++a; //(++a-->10+1)

console.log(a);
console.log(b);

//Post Increment--> increase the value by 1 after assigning

let c =10;
let d= c++; //(a++-->10)

console.log(c);11
console.log(d);10